use crate::octree::V3c;
use crate::spatial::math::{
    hash_direction, hash_region, octant_bitmask, position_in_bitmap_64bits,
    set_occupancy_in_bitmap_64bits,
};

#[allow(dead_code)]
fn generate_lut_64_bits() -> [[u64; 8]; 64] {
    // 64 poisitions, 8 directions
    let mut bitmap_lut = [[0u64; 8]; 64];

    //position
    for x in 0i32..4 {
        for y in 0i32..4 {
            for z in 0i32..4 {
                let bitmask_position =
                    position_in_bitmap_64bits(x as usize, y as usize, z as usize, 4);
                //direction
                for dx in -1i32..=1 {
                    for dy in -1i32..=1 {
                        for dz in -1i32..=1 {
                            // step through the matrix in the given direction and set relevant bits to occupied
                            if 0 == dx || 0 == dy || 0 == dz {
                                continue;
                            }
                            let direction_position =
                                hash_direction(&V3c::new(dx as f32, dy as f32, dz as f32));
                            let moved_x = (x + dx * 4).max(0).min(3);
                            let moved_y = (y + dy * 4).max(0).min(3);
                            let moved_z = (z + dz * 4).max(0).min(3);
                            let min_x = moved_x.min(x);
                            let max_x = moved_x.max(x);
                            let min_y = moved_y.min(y);
                            let max_y = moved_y.max(y);
                            let min_z = moved_z.min(z);
                            let max_z = moved_z.max(z);
                            let mut result_bitmask = 0;
                            for bx in min_x..=max_x {
                                for by in min_y..=max_y {
                                    for bz in min_z..=max_z {
                                        set_occupancy_in_bitmap_64bits(
                                            bx as usize,
                                            by as usize,
                                            bz as usize,
                                            4,
                                            true,
                                            &mut result_bitmask,
                                        );
                                    }
                                }
                            }
                            bitmap_lut[bitmask_position as usize][direction_position as usize] =
                                result_bitmask;
                        }
                    }
                }
            }
        }
    }
    bitmap_lut
}

#[allow(dead_code)]
fn generate_lut_8_bits() -> [[u8; 8]; 8] {
    // 8 poisitions, 8 directions
    let mut bitmap_lut = [[0u8; 8]; 8];

    //position
    for x in 0i32..2 {
        for y in 0i32..2 {
            for z in 0i32..2 {
                let bitmask_position = hash_region(&V3c::new(x as f32, y as f32, z as f32), 1.);
                //direction
                for dx in -1i32..=1 {
                    for dy in -1i32..=1 {
                        for dz in -1i32..=1 {
                            // step through the matrix in the given direction and set the positional bits to occupied
                            if 0 == dx || 0 == dy || 0 == dz {
                                continue;
                            }
                            let direction_position =
                                hash_direction(&V3c::new(dx as f32, dy as f32, dz as f32));
                            let moved_x = (x + dx * 2).max(0).min(2);
                            let moved_y = (y + dy * 2).max(0).min(2);
                            let moved_z = (z + dz * 2).max(0).min(2);
                            let min_x = moved_x.min(x);
                            let max_x = moved_x.max(x);
                            let min_y = moved_y.min(y);
                            let max_y = moved_y.max(y);
                            let min_z = moved_z.min(z);
                            let max_z = moved_z.max(z);

                            let mut result_bitmask = 0;
                            for bx in min_x..=max_x {
                                for by in min_y..=max_y {
                                    for bz in min_z..=max_z {
                                        result_bitmask |= octant_bitmask(hash_region(
                                            &V3c::new(bx as f32, by as f32, bz as f32),
                                            1.,
                                        ));
                                    }
                                }
                            }
                            bitmap_lut[bitmask_position as usize][direction_position as usize] =
                                result_bitmask;
                        }
                    }
                }
            }
        }
    }

    bitmap_lut
}

